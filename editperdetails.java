    public editperdetails() {
        setUndecorated(true);
        initComponents();
        PreparedStatement ps;
        ResultSet rs;
        String sql = "SELECT loginID FROM `login` WHERE sr=1";
        try{
            ps = myconnection.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();
            if(rs.next()){
                username=rs.getString(1);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        PreparedStatement pstmt;
        ResultSet r;
        String query = "SELECT Name,PRN,Gender,Phone,Email,Department from studentlogin WHERE username= '"+username+"'";
        try{
            pstmt = myconnection.getConnection().prepareStatement(query);
            r=pstmt.executeQuery();
            if(r.next()){
                name = r.getString(1);
                prn = r.getLong(2);
                gen = r.getString(3);
                ph = r.getLong(4);
                em = r.getString(5);
                dept = r.getString(6);
                namefld.setText(name);
                prnfld.setText(Long.toString(prn));
                genderfld.setText(gen);
                contactfld.setText(Long.toString(ph));
                emailfld.setText(em);
                deptfld.setText(dept);
            }
        }catch(Exception es){
            JOptionPane.showMessageDialog(null, es);
        }
        
    }

