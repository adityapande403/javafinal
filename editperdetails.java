    private void applybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applybtnActionPerformed
        // TODO add your handling code here:
        if(dobfld.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Enter DOB!!");
        }
        else{
            name=namefld.getText();
            prn=Long.parseLong(prnfld.getText());
            em=emailfld.getText();
            gen=genderfld.getText();
            dept=deptfld.getText();
            ph=Long.parseLong(contactfld.getText());
            String dob = dobfld.getText();
            String sql="update studentlogin set Name='"+name+"', PRN='"+prn+"', Email='"+em+"', Gender='"+gen+"', Department='"+dept+"', Phone='"+ph+"',DOB='"+dob+"' where username='"+username+"'";
        try{
            PreparedStatement ps;
            ps = myconnection.getConnection().prepareStatement(sql);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Edited Successfully!!");
        }catch(Exception es){
            Logger.getLogger(startupScreen.class.getName()).log(Level.SEVERE, null, es);
        }
        WelcomeStudent lgf=new WelcomeStudent();
        lgf.setVisible(true);
        lgf.setLocationRelativeTo(null);
        this.dispose();
        }
    }//GEN-LAST:event_applybtnActionPerformed

