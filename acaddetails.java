    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Statement stmt= myconnection.getConnection().createStatement();
            long p= Long.parseLong(prn.getText());
            String sql = "select * from studentlogin where PRN="+p;
            ResultSet rs = stmt.executeQuery(sql);
            if(rs.next()){
                float sem1= Float.parseFloat(jTextField1.getText());
                float sem2= Float.parseFloat(jTextField2.getText());
                float sem3= Float.parseFloat(jTextField3.getText());
                float sem4= Float.parseFloat(jTextField4.getText());
                float sem5= Float.parseFloat(jTextField5.getText());
                float sem6= Float.parseFloat(jTextField6.getText());
                float sem7= Float.parseFloat(jTextField7.getText());
                float ag=(sem1+sem2+sem3+sem4+sem5+sem6+sem7)/7;
                String query = "update studentlogin set Sem1="+sem1+",Sem2="+sem2+",Sem3="+sem3+",Sem4="+sem4+",Sem5="+sem5+",Sem6="+sem6+",Sem7="+sem7+",Aggregate='"+ag+"' WHERE PRN="+p;
                stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Marks Entered Successfully!!");
            }
            else{
                JOptionPane.showMessageDialog(this, "PRN not registered!!");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

